def extract_headings(pdf_file):
    """
    Extract headings from a PDF using PyMuPDF (fitz) if available,
    falling back to a simple font-size heuristic with pypdf.
    Returns (headings_list, has_outline).
    """
    pdf_bytes = pdf_file.read()
    headings = []
    has_outline = False

    try:
        import fitz  # PyMuPDF
        doc = fitz.open(stream=pdf_bytes, filetype='pdf')

        # Try document outline (bookmarks) first
        toc = doc.get_toc()
        if toc:
            has_outline = True
            for item in toc:
                level, title, page = item[0], item[1], item[2]
                if title.strip() and page > 0:
                    headings.append({'title': title.strip(), 'page': page, 'level': min(level, 4)})
            doc.close()
            return headings, has_outline

        # Fallback: font-size heuristic
        font_sizes = {}
        page_spans = []
        for page_num in range(len(doc)):
            page = doc[page_num]
            blocks = page.get_text("dict", flags=16)["blocks"]
            for block in blocks:
                for line in block.get("lines", []):
                    for span in line.get("spans", []):
                        size = round(span["size"])
                        text = span["text"].strip()
                        if text and len(text) > 2:
                            font_sizes[size] = font_sizes.get(size, 0) + 1
                            page_spans.append((page_num + 1, size, text, span.get("flags", 0)))

        doc.close()
        if not font_sizes:
            return [], False

        # Sizes above median are likely headings
        sorted_sizes = sorted(font_sizes.keys(), reverse=True)
        body_size = sorted(font_sizes.keys(), key=lambda s: font_sizes[s], reverse=True)[0]

        heading_sizes = [s for s in sorted_sizes if s > body_size * 1.05][:4]
        if not heading_sizes:
            return [], False

        size_to_level = {s: i + 1 for i, s in enumerate(sorted(heading_sizes, reverse=True))}

        seen = set()
        for page_num, size, text, flags in page_spans:
            if size in size_to_level and len(text) > 3:
                key = (text[:60], page_num)
                if key not in seen:
                    seen.add(key)
                    headings.append({
                        'title': text[:120],
                        'page': page_num,
                        'level': size_to_level[size],
                    })

        return headings, False

    except ImportError:
        pass

    # Final fallback: pypdf
    try:
        from pypdf import PdfReader
        import io
        reader = PdfReader(io.BytesIO(pdf_bytes))

        # Try outline
        if reader.outline:
            has_outline = True
            def walk(items, level=1):
                for item in items:
                    if isinstance(item, list):
                        walk(item, level + 1)
                    else:
                        try:
                            page_num = reader.get_destination_page_number(item) + 1
                            headings.append({
                                'title': str(item.title).strip(),
                                'page': page_num,
                                'level': min(level, 4),
                            })
                        except Exception:
                            pass
            walk(reader.outline)
            return headings, has_outline

        # No outline — return empty with note
        return [], False

    except Exception as e:
        raise RuntimeError(f"Could not parse PDF: {e}")


# ── Stripe checkout ──────────────────────────────────────────────

@login_required
@require_POST
def create_checkout_session(request):
    plan = request.POST.get('plan')
    if plan not in settings.STRIPE_PRICES:
        return JsonResponse({'error': 'Invalid plan'}, status=400)

    price_id = settings.STRIPE_PRICES[plan]
    if not price_id:
        return JsonResponse({'error': 'Stripe price not configured. Add STRIPE_PRICE_PRO / STRIPE_PRICE_PREMIUM to .env'}, status=400)

    profile = get_or_create_profile(request.user)

    # Create or reuse Stripe customer
    if not profile.stripe_customer_id:
        customer = stripe.Customer.create(
            email=request.user.email,
            metadata={'user_id': request.user.id},
        )
        profile.stripe_customer_id = customer.id
        profile.save()

    try:
        session = stripe.checkout.Session.create(
            customer=profile.stripe_customer_id,
            payment_method_types=['card'],
            line_items=[{'price': price_id, 'quantity': 1}],
            mode='subscription',
            success_url=request.build_absolute_uri('/dashboard/?checkout=success'),
            cancel_url=request.build_absolute_uri('/dashboard/?checkout=cancelled'),
        )
        return JsonResponse({'url': session.url})
    except stripe.error.StripeError as e:
        logger.error("Stripe checkout error: %s", e)
        return JsonResponse({'error': str(e)}, status=400)


@login_required
@require_POST
def create_portal_session(request):
    profile = get_or_create_profile(request.user)
    if not profile.stripe_customer_id:
        messages.error(request, 'No billing account found.')
        return redirect('dashboard')

    try:
        session = stripe.billing_portal.Session.create(
            customer=profile.stripe_customer_id,
            return_url=request.build_absolute_uri('/dashboard/'),
        )
        return redirect(session.url)
    except stripe.error.StripeError as e:
        logger.error("Stripe portal error: %s", e)
        messages.error(request, 'Could not open billing portal: ' + str(e))
        return redirect('dashboard')


# ── Stripe webhook ───────────────────────────────────────────────

@csrf_exempt
@require_POST
def stripe_webhook(request):
    payload = request.body
    sig_header = request.META.get('HTTP_STRIPE_SIGNATURE', '')
    webhook_secret = settings.STRIPE_WEBHOOK_SECRET

    try:
        if webhook_secret:
            event = stripe.Webhook.construct_event(payload, sig_header, webhook_secret)
        else:
            event = stripe.Event.construct_from(json.loads(payload), stripe.api_key)
    except (ValueError, stripe.error.SignatureVerificationError) as e:
        logger.warning("Webhook signature error: %s", e)
        return HttpResponse(status=400)

    _handle_stripe_event(event)
    return HttpResponse(status=200)


def _handle_stripe_event(event):
    data = event['data']['object']

    if event['type'] == 'customer.subscription.created':
        _sync_subscription(data)
    elif event['type'] == 'customer.subscription.updated':
        _sync_subscription(data)
    elif event['type'] == 'customer.subscription.deleted':
        _cancel_subscription(data)
    elif event['type'] == 'invoice.payment_failed':
        customer_id = data.get('customer')
        _update_subscription_status(customer_id, 'past_due')


def _sync_subscription(subscription):
    customer_id = subscription['customer']
    status = subscription['status']
    sub_id = subscription['id']

    try:
        profile = UserProfile.objects.get(stripe_customer_id=customer_id)
    except UserProfile.DoesNotExist:
        logger.warning("No profile for Stripe customer %s", customer_id)
        return

    profile.stripe_subscription_id = sub_id
    profile.subscription_status = status

    # Determine plan from price
    items = subscription.get('items', {}).get('data', [])
    if items:
        price_id = items[0]['price']['id']
        for plan_name, configured_price in settings.STRIPE_PRICES.items():
            if price_id == configured_price:
                profile.plan = plan_name
                break

    # Set subscription end date
    current_period_end = subscription.get('current_period_end')
    if current_period_end:
        profile.subscription_end = timezone.datetime.fromtimestamp(
            current_period_end, tz=timezone.utc
        )

    profile.save()
    logger.info("Synced subscription %s for %s → %s", sub_id, profile.user.email, profile.plan)


def _cancel_subscription(subscription):
    customer_id = subscription['customer']
    try:
        profile = UserProfile.objects.get(stripe_customer_id=customer_id)
        profile.plan = 'free'
        profile.subscription_status = 'canceled'
        profile.stripe_subscription_id = ''
        profile.save()
        logger.info("Canceled subscription for %s", profile.user.email)
    except UserProfile.DoesNotExist:
        pass


def _update_subscription_status(customer_id, status):
    try:
        profile = UserProfile.objects.get(stripe_customer_id=customer_id)
        profile.subscription_status = status
        profile.save()
    except UserProfile.DoesNotExist:
        pass


# ── Password change override ─────────────────────────────────────────────────
# allauth 0.57+ bundles its own templates that take priority even with custom
# loaders. We override the URL before allauth.urls and render our own template
# explicitly, using allauth's form and adapter so all logic stays intact.

from django.contrib.auth.decorators import login_required
from django.shortcuts import render, redirect
from django.contrib import messages as django_messages

@login_required
def password_change(request):
    from allauth.account.forms import ChangePasswordForm
    from allauth.account.adapter import get_adapter

    if request.method == 'POST':
        form = ChangePasswordForm(data=request.POST, user=request.user)
        if form.is_valid():
            form.save()
            get_adapter(request).add_message(
                request,
                django_messages.SUCCESS,
                'account/messages/password_changed.txt',
            )
            return redirect('dashboard')
    else:
        form = ChangePasswordForm(user=request.user)

    return render(request, 'account/password_change.html', {'form': form})


