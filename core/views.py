def upload_pdf(request):
    """
    Accepts a PDF, runs the heading-extraction logic, returns JSON.
    No file is stored. We only record metadata for rate-limiting.
    """
    if request.method != 'POST':
        return JsonResponse({'error': 'POST required'}, status=405)

    pdf_file = request.FILES.get('pdf')
    if not pdf_file:
        return JsonResponse({'error': 'No file provided'}, status=400)

    file_size = pdf_file.size

    # ── Determine plan & enforce limits ──────────────────────────
    if request.user.is_authenticated:
        profile = get_or_create_profile(request.user)
        plan = profile.effective_plan
        limits = profile.limits

        max_bytes = limits['max_mb'] * 1024 * 1024
        if file_size > max_bytes:
            return JsonResponse({'error': f"File exceeds {limits['max_mb']} MB limit for your plan."}, status=400)

        if limits['daily_uploads'] is not None:
            if profile.uploads_today() >= limits['daily_uploads']:
                return JsonResponse({'error': f"Daily upload limit of {limits['daily_uploads']} reached. Upgrade for more."}, status=429)

        watermark = limits['watermark']

        # Record upload (metadata only, no file stored)
        UploadRecord.objects.create(
            user=request.user,
            filename=pdf_file.name[:255],
            file_size=file_size,
        )

    else:
        # Anonymous user
        plan_limits = settings.PLAN_LIMITS['anonymous']
        max_bytes = plan_limits['max_mb'] * 1024 * 1024
        if file_size > max_bytes:
            return JsonResponse({'error': f"File exceeds {plan_limits['max_mb']} MB limit. Sign up for more."}, status=400)

        ip, _ = get_client_ip(request)
        today = timezone.now().date()
        anon_count = AnonymousUploadRecord.objects.filter(ip_address=ip, created_at__date=today).count()
        if anon_count >= plan_limits['daily_uploads']:
            return JsonResponse({'error': 'Daily limit reached. Sign up for free to get 5 uploads/day.'}, status=429)

        AnonymousUploadRecord.objects.create(ip_address=ip)
        watermark = plan_limits['watermark']
        plan = 'anonymous'

    # ── Extract headings from PDF ─────────────────────────────────
    try:
        headings, has_outline = extract_headings(pdf_file)
    except Exception as e:
        logger.exception("PDF extraction error")
        return JsonResponse({'error': 'Could not read PDF: ' + str(e)}, status=400)

    return JsonResponse({
        'headings': headings,
        'has_outline': has_outline,
        'watermark': watermark,
        'plan': plan,
    })


